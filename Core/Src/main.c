  MX_GPIO_Init();
  MX_TIM2_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */
  
  ring_buffer_init(&usart_tx_buf);
  state = SAMPLE_COMPLETE; // Starting state - Wait for first timer overflow interrupt
  start_timer2();

  char str_buf[40];
  uint16_t time_us;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1) {
	  switch (state) {
      	  case TRIG_BEGIN:
      		  GPIOA->BSRR = HCSR04_TRIG_Pin;
      		  while (state != TRIG_END);
      		  break;
      	  case TRIG_END:
      		  GPIOA->BRR = HCSR04_TRIG_Pin;
      		  SLEEP_MODE();
      		  break;
      	  case SAMPLE_BEGIN:
      		  SLEEP_MODE();
      		  break;
      	  case SAMPLE_END:
      		  time_us = timer2_ic_falling_cnt - timer2_ic_rising_cnt;
      		  output_reading(str_buf, time_us);
      		  state = SAMPLE_COMPLETE; // Go back to sleep while waiting for next overflow
      		  break;
      	  case SAMPLE_COMPLETE:
      		  SLEEP_MODE();
      		  break;
      	  default:
      		  break;
    }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
